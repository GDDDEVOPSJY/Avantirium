                    <p>
                        Many cloud hosting implementations start with one area of the business then look to expand onward after initial success. Scaling any software across an organization will have challenges, and cloud is no exception. Not only is there the question of scaling being cost-prohibitive, but organizations sometimes struggle to move beyond the initial use case and get buy-in from other business units.
                    </p>
                    
                    <h2 class="mt-4 mb-2">Cloud Service Models and Responsibilities</h2>
                    
                    <p>
                    	Cloud security is not a one-sided responsibility vested with Cloud Service Provider (CSP) or customer. Moreover, it is a shared responsibility model between the cloud service provider and the client. Managing the cloud security flawlessly is crucial to the successful running of business. 
                    </p>
                    
                    <p>
                    	The three major service models followed in cloud computing are:
                    </p>
                    
                    <ol type="1">
                        
                        <li><b>Infrastructure as a Service (IaaS):</b> In this model, cloud provider is responsible for physical security of the infrastructure and network. Customer is responsible for all the applications and services built on the infrastructure.</li>
                        <li><b>Platform as a Service (PaaS):</b> Here CSP takes the responsibility of virtual machines, operating system, middleware technologies while customer is responsible for applications, data, and interfaces that are deployed on the platform.</li>
                        <li><b>Software as a Service (SaaS):</b> In the SaaS model, cloud service provider is responsible for everything except the application data and user interfaces.</li>
                        
                    </ol>
                    
                    <p>
                        Security vulnerabilities and risk elements are high in the cloud. So it is important to understand the security challenges and the roles and responsibilities of parties involved. By taking proper security measures you can keep the challenges at bay. 
                    </p>
                    
                    <p>
                        Below summarizes a few best practices to be followed.
                    </p>
                    
                    <h2 class="mt-4 mb-2">Security Challenges</h2>
                    
                    <p>
                    	Security challenges in the cloud environment are considerably high, no matter how many volumes of server migrations happened. The major challenges faced by infra team while migrating from on-premises to cloud are mainly in the areas of data ownership, data control and security and migration methodology. Failure in any of that area will leave the organization vulnerable and that might trigger data exposure and security breach. 
                    </p>
                    
                    <p>
                        The three key areas that to be taken into account while implementing cloud security is:
                    </p>
                    
                    <ol type="1">
                        
                        <li>
                            <p><b>Organization level Security policy:</b> This broadly covers meeting the compliance and regulation policies prevailed in geographic location of data (eg: HIPAA, when you handle medical related data from US). This further demands in new business processes implementation and new skills learning. Data security now is a shared responsibility between cloud facilitator and an organization. Both parties have crucial role to play in guarding data against security breaches.  Cloud security policies should be separately defined or existing ones to be redefined to meet cloud infrastructure monitoring and governing.</p>
                            <p>While defining the security policies, cloud migration experts should consider the following:</p>
                            
                            <ul>
                                <li>Revise existing security policies proportional to your cloud infrastructure requirement.</li>
                                <li>Implement systematic security practices for monitoring your cloud space.</li>
                                <li>Apply impeccable data protection policies and periodic auditing.</li>
                                <li>Risk monitoring and faster remediation.</li>
                                <li>Data transmission policies to control data movement and storage (both in transit and rest).</li>
                                <li>Resource training and security policy execution.</li>
                                <li>Security checks mock-drills.</li>
                            </ul>
                        </li>
                        
                        <li>
                            <b>Security of Data protection while in transit:</b> Data security during transmission, both inflow and outflow happens. So proper data encryption methods should be adapted while transmitting data across channels to protect data security. Many enterprises opted hybrid cloud hosting with their data stored in cloud and on-premises as well. Data exposure is the main threat that to be handled during data movement. Advanced threat protection and malware prevention systems to be properly placed. SSL, VPN enabled access, and encryption are effective secure measures to prevent vulnerabilities in data transmission.
                        </li>
                        
                        <li>
                            <b>Security of Data protection at rest:</b> Even when data resides in your servers security vulnerabilities are there. Data protection methods like encryption, firewall, anti-virus software etc. to be incorporated as security layers.  Data access controls to be implemented to restrict data availability to end-users.
                        </li>
                        
                    </ol>
                    
                    <p>
                        Cloud server security is a mutual responsibility between host provider and enterprise. By strictly following the above outlined best practices and processes defined in your security policy will help prevent data attacks and breaches.
                    </p>
                    
